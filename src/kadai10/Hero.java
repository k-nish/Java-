package kadai10;

public class Hero {
	private int HP;
	private String name;
	private int MP;

	public int getHp() {
		return this.HP;
	}

	public void setHp(int HP) {
		this.HP = HP;
	}

	public String getName() {
		return this.name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getMp() {
		return this.MP;
	}

	public void setMp(int MP) {
		this.MP = MP;
	}
}
